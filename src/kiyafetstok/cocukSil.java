    public void listele() {
        cocukdao cdao = new cocukdao();
        cdao.findAll();
        ArrayList<cocuk> cocuklistesi = new ArrayList<>();

        cocuklistesi = (ArrayList<cocuk>) cdao.findAll();
        String dizim[][] = new String[cocuklistesi.size()][];

        for (int i = 0; i < cocuklistesi.size(); i++) {
            dizim[i] = new String[]{
                String.valueOf(cocuklistesi.get(i).getCocuk_id()),
                valueOf(cocuklistesi.get(i).getRenk()),
                valueOf(cocuklistesi.get(i).getBeden()),
                valueOf(cocuklistesi.get(i).getTur()),
                valueOf(cocuklistesi.get(i).getStoksayisi()),
                valueOf(cocuklistesi.get(i).getFiyat()),
                valueOf(cocuklistesi.get(i).getTur_id())

            };

        }
        TableModel tabloModeli = new DefaultTableModel(
                dizim,
                new String[]{"erkek_id", "renk", "marka", "beden", "stoksayisi", "fiyat", "tur_id"}
        );

        cocuktablo.setModel(tabloModeli);

        System.out.println(cdao.findAll());
    }

